void		ft_sort_int_tab(int *tab, int size)
{
	int temp;
	int i;
	int j;

	j = 0;
	while (j != size - 1)
	{
		i = 0;
		j++;
		while (i < size - j)
		{
			if (tab[i] > tab[i + 1])
			{
				temp = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = temp;
			}
			i++;
		}
	}
}

int		ft_strcmp(char *s1, char *s2)
{
	char *a;
	char *b;

	a = s1;
	b = s2;
	while (1)
	{
		if (*a != *b)
		{
			return (*a > *b ? 1 : -1);
		}
		if (*a == '\0' && *b == '\0')
			return (0);
		if (*a == '\0')
		{
			return (-1);
		}
		if (*b == '\0')
		{
			return (1);
		}
		a++;
		b++;
	}
}

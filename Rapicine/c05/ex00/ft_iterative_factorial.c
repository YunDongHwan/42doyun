int		ft_iterative_factorial(int nb)
{
	int	i;
	int	num;

	i = 0;
	num = 1;
	if (nb < 0)
		return (0);
	while (i < nb)
	{
		num *= nb - i;
		i++;
	}
	return (num);
}

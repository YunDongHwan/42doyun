#include <unistd.h>

void		ft_print_comb(void)
{
	char i;
	char j;
	char k;

	i = '0' - 1;
	j = '0';
	k = '1';
	while (i < '7')
	{
		i = i + 1;
		j = i;
		while (j < '8')
		{
			j = j + 1;
			k = j;
			while (k < '9')
			{
				k = k + 1;
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &k, 1);
			}
		}
	}
	return ;
}

#include <unistd.h>

void		ft_increse_n(char f_1, char f_2, char n_1, char n_2)
{
	while (n_1 <= '9')
	{
		while (n_2 <= '9')
		{
			write(1, &f_1, 1);
			write(1, &f_2, 1);
			write(1, " ", 1);
			write(1, &n_1, 1);
			write(1, &n_2, 1);
			if (f_1 == '9' && f_2 == '8')
			{
				return ;
			}
			write(1, ",", 1);
			n_2 = n_2 + 1;
		}
		n_1 = n_1 + 1;
		n_2 = '0';
	}
}

void		ft_print_comb2(void)
{
	char f_1;
	char f_2;
	char n_1;
	char n_2;

	f_1 = '0';
	f_2 = '0';
	n_1 = '0';
	n_2 = '1';
	while (f_1 <= '9')
	{
		while (f_2 <= '9')
		{
			ft_increse_n(f_1, f_2, n_1, n_2);
			n_1 = f_1;
			f_2 += 1;
			n_2 = f_2 + 1;
		}
		f_2 = '0';
		f_1 = f_1 + 1;
	}
}
